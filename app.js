/// ..............VARIABLES..............

// In JavaScript, variables are categorized based on how they are declared:

// 1. **`var`**: Declares a function-scoped or globally-scoped variable, and it can be re-declared and updated.
// 2. **`let`**: Declares a block-scoped variable, which can be updated but not re-declared within the same scope.
// 3. **`const`**: Declares a block-scoped constant, which cannot be updated or re-declared after its initial assignment.

// These are the main types of variable declarations in JavaScript.

// Variable: basically is leyee declare kya jata hay k ap memoray may aik space allocate(مقرر کرنا) krtay hay, phir jaha hum values ko store krtay hay.
// var: is a keyword jo variable ko declare krnay may kam ata hay
// equla to (=) : is a assigning operator, jis may 5 assign kr deya jo kay aik value hay.
// 5 : is a value.
// ; :  is not a 'Semicolon', it is a statement terminator.

// Sure! Let's break down `var a = 5;` in simple terms:

// 1. **`var`**: This is a keyword used in JavaScript to declare a variable. A variable is like a container where you can store a value (like a number, text, etc.).

// 2. **`a`**: This is the name of the variable (also called the variable name or identifier). You can choose almost any name for your variables, but in this case, it's called `a`.

// 3. **`=`**: This is the assignment operator. It assigns the value on the right side (`5`) to the variable on the left side (`a`).

// 4. **`5`**: This is the value you're storing in the variable `a`. Here, `5` is a number.

// ### Putting it all together:
// When you write `var a = 5;`, you are creating a variable called `a` and storing the number `5` in it. Now, whenever you use `a` in your code, it will represent the number `5` until you change its value.

// For example, if you later write `console.log(a);`, it will print `5`.

